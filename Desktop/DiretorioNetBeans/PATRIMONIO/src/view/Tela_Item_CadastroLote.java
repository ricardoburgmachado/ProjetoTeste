    public int obtemIndexSelecLocalizacao(){
       
      
       try{
           ConectaBD conectaDB = new ConectaBD();
           conectaDB.select("*", "localizacao"," order by idlocalizacao asc");
           
           int idBD = 0;
           int cont = 1; 
           while( conectaDB.getResultSet().next() ){

               if( cont == ComboBoxLocalizacao.getSelectedIndex()  ){
               
                   idBD = conectaDB.resultSet.getInt("idlocalizacao");
               }
               cont++;
           } 
           
           //System.out.println( "LOCALIZAÇÃO DB: "+idBD);
           return idBD;
           
        }catch(Exception erro){
        
            JOptionPane.showMessageDialog(null, "Erro ao obter dados, erro: "+erro);
        }    
        
       return 0;
    }
    
