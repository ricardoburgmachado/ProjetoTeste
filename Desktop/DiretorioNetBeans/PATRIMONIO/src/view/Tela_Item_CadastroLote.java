    public void preencheComboBoxLocalizacao(){
        
        ComboBoxLocalizacao.removeAllItems();
       try{
           ConectaBD conectaDB = new ConectaBD();

           conectaDB.select("*", "localizacao"," order by idlocalizacao asc");

           ComboBoxLocalizacao.addItem("<<            Selecione            >>");

           while( conectaDB.getResultSet().next() ){

                ComboBoxLocalizacao.addItem( conectaDB.resultSet.getString("idlocalizacao").toString()+" | Prédio: "+conectaDB.resultSet.getString("predio").toString()+" | Sala: "+conectaDB.resultSet.getString("sala").toString());
           } 
                    
        }catch(Exception erro){
        
            JOptionPane.showMessageDialog(null, "Erro ao listar dados, erro: "+erro);
            
        }
    }

